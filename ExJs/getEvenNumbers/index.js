// השלם את הפונקציה הבאה כך שתחזיר מערך של המספרים הזוגיים בלבד מהמערך הנתון
function getEvenNumbers(numbers) {
    // השלם את הקוד כאן
    return numbers.filter(number=> number % 2 === 0);
  }
  
  // דוגמה: getEvenNumbers([1, 2, 3, 4, 5, 6]) צריך להחזיר [2, 4, 6]



  
